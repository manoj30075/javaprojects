public class ChessPiece {
    char chessPieceColor;
    int noOfPieces = 0;
    int maxNoOfPieces = 0;

    public char getChessPieceColor() {
        return chessPieceColor;
    }

    public void setNoOfPieces(int noOfPieces) {
        this.noOfPieces = noOfPieces;
    }

    public int getNoOfPieces() {
        return noOfPieces;
    }

    public void setMaxNoOfPieces(int maxNoOfPieces) {
        this.maxNoOfPieces = maxNoOfPieces;
    }

    public int getMaxNoOfPieces() {
        return maxNoOfPieces;
    }
}
